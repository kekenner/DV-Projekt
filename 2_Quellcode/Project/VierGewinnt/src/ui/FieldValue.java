package ui;

/**
 * Das Enum FieldValue wird erstellt. Enums sind Variablentypen mit festgelegten Wertemöglichkeiten.
 * Jedes einzelne Feld kann nachher den Wert "Spieler1" , "Spieler2" oder "Empty" annehmen.
 * 
 * @author Marius Mauth, Simon Fluck
 */
public enum FieldValue {                  

	/**
	 * Spieler 1
	 */
	Spieler1,
	/**
	 * Spieler 2
	 */
	Spieler2,
	/**
	 * Empty
	 */
	EMPTY;            
}
